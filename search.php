<?php
/**
 * The template for displaying Search Results pages.
 *
 * @package adamjang
 */

if($_SERVER['HTTP_X_REQUESTED_WITH']==''):
    get_header();
endif;

?>

	<section id="primary" class="content-area">
		<main id="main" class="site-main" role="main">

		<?php if ( have_posts() ) : ?>

			<header class="page-header">
				<h1 class="page-title"><?php printf( __( 'Search Results for: %s', 'adamjang' ), '<span>' . get_search_query() . '</span>' ); ?></h1>
			</header><!-- .page-header -->

			<?php /* Start the Loop */ ?>
			<?php while ( have_posts() ) : the_post(); ?>

				<?php get_template_part( 'content', 'search' ); ?>

			<?php endwhile; ?>

			<?php adamjang_paging_nav(); ?>

		<?php else : ?>

			<?php get_template_part( 'content', 'none' ); ?>

		<?php endif; ?>

		</main><!-- #main -->
	</section><!-- #primary -->

<?php
if($_SERVER['HTTP_X_REQUESTED_WITH']==''):
	get_sidebar();
    get_footer();
endif;
?>
